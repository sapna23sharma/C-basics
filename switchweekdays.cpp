#include<iostream>
using namespace std;
main()
{
	int n;
	cout<<"enter the day :\n";
	cin>>n;
	switch(n)
	{
		case 1:
			cout<<"monday";
			break;
			case 2:
				cout<<"tuesday";
				break;
				case 3:
					cout<<"wednesday";
					case 4:
						cout<<"thrusday";
						case 5:
							cout<<"friday";
							case 6:
								cout<<"saturday";
								case 7:
									cout<<"sunday";
									default:
										cout<<"invalid";
	}
}
