#include<iostream>
using namespace std;
main()
{
	int a=5;
	int b=5;
	cout<<"value of a==b"<<(a==b)<<endl;
	cout<<"value of a>b"<<(a>b)<<endl;
	cout<<"value of a<b"<<(a<b)<<endl;
	cout<<"value of a>=b"<<(a>=b)<<endl;
	cout<<"value of a<=b"<<(a<=b)<<endl;
	cout<<"value of a!=b"<<(a!=b)<<endl;
}
